document.getElementById("sendEmail").addEventListener("click", function () {
    const email = "phemiejhay@gmail.com"; // Replace with the recipient's email address
    const subject = "I Need Support with [Your Project Name] "; // Replace with your desired subject

    const emailLink = "mailto:" + email + "?subject=" + encodeURIComponent(subject);

    // Create a hidden link element and trigger a click event to open the email client
    const link = document.createElement("a");
    link.href = emailLink;
    link.style.display = "none";
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
});

